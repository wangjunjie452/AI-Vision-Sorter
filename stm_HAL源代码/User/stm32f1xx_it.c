/**
  * @brief  USART1 中断入口
  */
void USART1_IRQHandler(void)
{
    HAL_UART_IRQHandler(&huart1);
}

/**
  * @brief  USART1 接收完成回调：将字节存入串口解析模块的环形缓冲区
  * @note   HAL_UART_Receive_IT 内部已将 DR 读入 rx_byte_buf，
  *         回调中直接使用该缓冲区，禁止再读 DR 寄存器（会得到脏数据）
  *         rx_byte_buf 定义在 serial_parser.c，通过 extern 引用
  */
extern uint8_t rx_byte_buf;  /* serial_parser.c 中定义 */

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART1)
    {
        Serial_RxByte(rx_byte_buf);
        /* 重新开启下一次 1 字节中断接收（复用同一个缓冲区） */
        HAL_UART_Receive_IT(&huart1, &rx_byte_buf, 1);
    }
}


